#slicing is using slice syntax to return lenght of characters.
a = 'Hello, World!'
print(a[2:5])

#slice from the start.
print(a[:5])

#slice from the end.
print(a[2:])

#negative index start the slice from the end of the string.
print(a[-5:-2])

#python includes the character at index start, but excludes the character at index end.